package nl.ordina.jtech.hackadrone.utils

/**
 * Class representing ANSI colors.

 * @author Nils Berlijn
 * *
 * @version 1.0
 * *
 * @since 1.0
 */
object ANSI {

    /**
     * The ANSI code for the reset colors.
     */
    val RESET = "\u001B[0m"

    /**
     * The ANSI code for the color black.
     */
    val BLACK = "\u001B[30m"

    /**
     * The ANSI code for the color red.
     */
    val RED = "\u001B[31m"

    /**
     * The ANSI code for the color green.
     */
    val GREEN = "\u001B[32m"

    /**
     * The ANSI code for the color yellow.
     */
    val YELLOW = "\u001B[33m"

    /**
     * The ANSI code for the color blue.
     */
    val BLUE = "\u001B[34m"

    /**
     * The ANSI code for the color purple.
     */
    val PURPLE = "\u001B[35m"

    /**
     * The ANSI code for the color cyan.
     */
    val CYAN = "\u001B[36m"

    /**
     * The ANSI code for the color white.
     */
    val WHITE = "\u001B[37m"

}
